public class ExpressoesComparativas {

    public static void main(String[] args) {
        int x = 5;

        boolean op01 = x > 0;
        boolean op02 = x == 3;
        boolean op03 = 10 <= 30;
        boolean op04 = x != 2;

        System.out.println("x > 0? Resposta = " + op01);
        System.out.println("x == 3? Resposta = " + op02);
        System.out.println("10 <= 30? Resposta = " + op03);
        System.out.println("x != 2? Resposta = " + op04);
    }
}
